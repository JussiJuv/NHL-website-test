class TeamStats {
    constructor(id, losses, ot, wins, points, divRank, confRank, leagRank, gamesPlayed) {
        this.id = id;
        this.losses = losses;
        this.ot = ot;
        this.wins = wins;
        this.points = points;
        this.divRank = divRank;
        this.confRank = confRank;
        this.leagRank = leagRank;
        this.gamesPlayed = gamesPlayed;
    }
} 